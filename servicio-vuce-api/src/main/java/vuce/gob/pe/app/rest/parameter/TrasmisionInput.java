public class TrasmisionInput {

	private Integer vcId;
	private String vcTransaccion;
	private Integer veId;
	private String veTransaccion;
		
	public Integer getVcId() {
		return vcId;
	}
	public void setVcId(Integer vcId) {
		this.vcId = vcId;
	}
	public String getVcTransaccion() {
		return vcTransaccion;
	}
	public void setVcTransaccion(String vcTransaccion) {
		this.vcTransaccion = vcTransaccion;
	}
	public Integer getVeId() {
		return veId;
	}
	public void setVeId(Integer veId) {
		this.veId = veId;
	}
	public String getVeTransaccion() {
		return veTransaccion;
	}
	public void setVeTransaccion(String veTransaccion) {
		this.veTransaccion = veTransaccion;
	}
	
	
	
}
